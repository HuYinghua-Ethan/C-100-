/*
题目：809*??=800*??+9*?? 其中??代表的两位数, 809*??为四位数，8*??的结果为两位数，9*??的结果为3位数。求??代表的两位数，及809*??后的结果。
*/

#include <stdio.h>

int main()
{
    int i, b, a;
    a = 809;

    for(i = 10; i < 100; i++)
    {
        b = a * i;
        if(b > 1000 && b < 10000 && 8 * i < 100 && 9 * i > 100)
            printf("%d = 800 * %d + 9 * %d\n", b, i, i);
    }
    
    return 0;
}


